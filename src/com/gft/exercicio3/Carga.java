package com.gft.exercicio3;

public class Carga{
    Double valor;
    Double peso;

    public Carga(Double valor, Double peso) {
        this.valor = valor;
        this.peso = peso;
    }

    public Double getValor() {
        return valor;
    }

    public void setValor(Double valor) {
        this.valor = valor;
    }

    public Double getPeso() {
        return peso;
    }

    public void setPeso(Double peso) {
        this.peso = peso;
    }
}
